var openNav;
var closeNav;

function openNav() {
    document.getElementById("mySidenav").style.width = "350px";
    document.getElementById("#main").style.marginLeft = "350px";
}

function closeNav() {
    document.getElementById("mySidenav").style.width = "0";
    document.getElementById("#main").style.marginLeft= "0";
}

	